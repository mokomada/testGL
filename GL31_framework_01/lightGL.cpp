//=============================================================================
//	関数名	:Set
//	引数	:無し
//	戻り値	:無し
//	説明	:カメラを設定する。
//=============================================================================
void CLightGL::Set(void)
{
	GLfloat lightPos1[4]	= { 0.0f, 1.0f, 1.0f, 0.0f };
	GLfloat lightCol[4]		= {1.0f, 1.0f, 1.0f, 1.0f};

	glLightfv(GL_LIGHT0, GL_POSITION, lightPos1);
	glLightfv(GL_LIGHT0, GL_DIFFUSE, lightCol);
}